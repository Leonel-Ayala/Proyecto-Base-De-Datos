--BUSCA ID_TRATAMIENTO

CREATE OR REPLACE FUNCTION LAROATLB_BUSCA_TRATA(F_ID_MASCOTA NUMBER)
RETURN NUMBER
IS 
    NUM_TRATA NUMBER :=0;
BEGIN
    SELECT ID_TRATAMIENTO INTO NUM_TRATA
    FROM LAROATLB_TRATAMIENTO
    WHERE FECHA=SYSDATE AND ID_MASCOTA=F_ID_MASCOTA
    RETURN NUM_TRATA;
END;

